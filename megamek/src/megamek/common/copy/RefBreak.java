package megamek.common.copy;

public interface RefBreak<T> {

    /**
     * Copy the object, creating a new instance with the same values. This is NOT a deep or a perfect copy, it is
     * instead a "serviceable" copy. It allows to create a new instance that has NO references to the previous one, so
     * it can run on a different thread or be modified without affecting the original object. If the object it returns
     * is incomplete for your purposes, you should override this method and complete it or expand the implementation.
     *
     * @return a new instance of the object with the same values.
     */
    T copy();

}
