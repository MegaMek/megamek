package megamek.common.net.marshalling;

/**
 * Custom exception for receipt of an Invalid Packet Command received over the network. Pass in the integer received and
 * this passes up a custom message with said number.
 */
public class InvalidPacketCommandReceivedException extends Exception {
    public InvalidPacketCommandReceivedException(Integer message) {
        super(String.format("Invalid packet command Received: %d", message));
    }
}
